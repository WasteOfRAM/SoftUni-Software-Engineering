function employeesFilter(data, criteria) {
    let employeeData = JSON.parse(data);

    if (criteria !== 'all') {
        let criteriaArg = criteria.split('-');
        let criteriaType = criteriaArg[0];
        let criteriaValue = criteriaArg[1];

        employeeData = employeeData.filter((e) => e[criteriaType] === criteriaValue);
    }

    for (let i = 0; i < employeeData.length; i++) {
        let currentEmployee = employeeData[i];
        console.log(`${i}. ${currentEmployee.first_name} ${currentEmployee.last_name} - ${currentEmployee.email}`);
    }
}

